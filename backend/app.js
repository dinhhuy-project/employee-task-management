var createError = require("http-errors");
var express = require("express");
const cors = require("cors");
var path = require("path");
var cookieParser = require("cookie-parser");
var logger = require("morgan");

var ownerAuthRouter = require("./routes/ownerAuth.routes");
var employeeAuthRouter = require("./routes/employeeAuth.routes");
var employeeRouter = require("./routes/employee.routes");
var taskRouter = require("./routes/task.routes");
var messageRouter = require("./routes/message.routes");

var app = express();

app.use(
  cors({
    origin: "*",
    methods: ["GET", "POST", "PUT", "DELETE", "PATCH", "OPTIONS"],
    allowedHeaders: ["Content-Type", "Authorization"],
  }),
);
app.use(express.json());

// view engine setup
app.set("views", path.join(__dirname, "views"));
app.set("view engine", "pug");

app.use(logger("dev"));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, "public")));

app.use("/api/owner/auth", ownerAuthRouter);
app.use("/api/employee/auth", employeeAuthRouter);
app.use("/api/owner/employees", employeeRouter);
app.use("/api/tasks", taskRouter);
app.use("/api/messages", messageRouter);

// catch 404 and forward to error handler
app.use(function (req, res, next) {
  next(createError(404));
});

// error handler
app.use(function (err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get("env") === "development" ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render("error");
});

module.exports = app;
